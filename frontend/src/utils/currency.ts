export const getCurrencySymbol = ({
  locale,
  currency,
}: {
  locale?: string;
  currency: string;
}) => {
  return (0)
    .toLocaleString(locale ?? 'en-US', {
      style: 'currency',
      currency,
      minimumFractionDigits: 0,
      maximumFractionDigits: 0,
    })
    .replace(/\d/g, '')
    .trim();
};

export const calculatePrice = ({
  price,
  discount,
  quantity,
  exchangeRate,
}: {
  price: number;
  discount?: number;
  quantity?: number;
  exchangeRate?: number;
}) => {
  const priceWithRate = price * (exchangeRate ?? 1);
  const subtotal = priceWithRate * (quantity ?? 1);
  const discounted = (subtotal * (discount ?? 1)) / 100;
  const total = subtotal - discounted;

  return {
    unitPrice: priceWithRate,
    subtotal,
    total,
    discounted,
  };
};

export const formatPrice = (
  price: number,
  options?: Omit<Intl.NumberFormatOptions, 'currency'> & {
    locale?: string | false; // false is in case I wan't to disable the locale
    currency?: string | null;
  },
) => {
  const locale =
    options?.locale === false ? undefined : options?.locale || 'en-US';

  return price.toLocaleString(locale, {
    style: 'currency',
    ...options,
    currency: options?.currency || 'USD',
  });
};

